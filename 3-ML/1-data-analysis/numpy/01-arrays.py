import numpy as np

# 1D arrays
arr = np.array([1, 2, 3, 4, 5])
arr1 = np.array([10, 20, 30])
print(arr)
print(arr1)

# Element-wise operations
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])
print(a + b)
print(a * b)

# 2D array (matrix)
matrix = np.array([
    [1, 2, 3],
    [4, 5, 6]
])
print(matrix)
print(matrix.shape)
